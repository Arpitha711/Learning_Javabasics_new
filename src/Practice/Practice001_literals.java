package Practice;

public class Practice001_literals {
    public static void main(String[] args) {
        int decimal = 25;      // Decimal literal
        int octal = 031;       // Octal literal (starts with 0)
        int hex = 0x19;        // Hexadecimal literal (starts with 0x)
        int binary = 0b11001;  // Binary literal (starts with 0b)

        System.out.println("Decimal: " + decimal);
        System.out.println("Octal: " + octal);
        System.out.println("Hexadecimal: " + hex);
        System.out.println("Binary: " + binary);
    }
}
