package java_excersice02;

public class Lab011_Constants {
    public static void main(String[] args) {
        //Constants - once created cannot be modified
        //ex: PI= 3.14
        //Contants can be created with final key word
        final float pi = 3.14f;
        //pi = 5; give an error
        System.out.println(pi);


    }
}
