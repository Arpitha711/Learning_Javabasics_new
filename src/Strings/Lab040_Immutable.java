package Strings;

public class Lab040_Immutable {
    public static void main(String[] args) {
//        Immutability**: **Strings in Java are immutable**, meaning once a string object is created,
//        its value cannot be changed. Any modification results in the creation of a new string instance.


        String name = "Arpitha";
        name = "M";
        System.out.println(name);
    }
}
